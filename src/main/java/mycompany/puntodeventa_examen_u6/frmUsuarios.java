    private void btnEliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEliminarActionPerformed
        int indice = tblUsuarios.getSelectedRow();
        //Verificar que haya algo seleccionado
        if(indice >= 0){
            //Muestra una ventana de confirmacion con los botones SI y NO
            int respuesta = JOptionPane.showConfirmDialog(this, "Â¿Esta seguro "
                    +"que desea eliminar al usuario " + tblUsuarios.getValueAt(indice, 0), 
                    "Gestion de alumnos", JOptionPane.YES_NO_OPTION);
            if(respuesta == JOptionPane.YES_OPTION){
                //enviamos la posicion de la linea a areeemplazar, el alumno con las modificaciones
                //en version String y obtenemos la actualizacion de las lineas
                ArrayList<String> lineas = ManejadorArchivo.eliminarLinea("Usuarios.txt", indice);
                if(lineas!=null){
                    //Si la eliminacion se hizo correctamente informamos
                    JOptionPane.showMessageDialog(this,"El Usuario se ha eliminado correctamente",
                            "Gestion de Usuarios", JOptionPane.INFORMATION_MESSAGE);
                    //Actualizamos la tabla
                    actualizarTabla(lineas);
                } else {
                    //Informar que no se hizo la operacion
                    JOptionPane.showMessageDialog(this,"La eliminacion no pudo ser completada", 
                            "Gestion de Usuarios", JOptionPane.ERROR_MESSAGE);
                }
            }
        } else {
            JOptionPane.showMessageDialog(this, "Selecciona un Usuario", 
                    "Gestion de Usuarios", JOptionPane.WARNING_MESSAGE);
        }
    }//GEN-LAST:event_btnEliminarActionPerformed

